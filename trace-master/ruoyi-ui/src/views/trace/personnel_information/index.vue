<template>
  <div class="app-container">
    <el-form :model="queryParams" ref="queryForm" size="small" :inline="true" v-show="showSearch" label-width="68px">
      <el-form-item label="编号" prop="personnelInformationId">
        <el-input
          v-model="queryParams.personnelInformationId"
          placeholder="请输入编号"
          clearable
          @keyup.enter.native="handleQuery"
        />
      </el-form-item>
      <el-form-item label="姓名" prop="personnelInformationName">
        <el-input
          v-model="queryParams.personnelInformationName"
          placeholder="请输入姓名"
          clearable
          @keyup.enter.native="handleQuery"
        />
      </el-form-item>
      <el-form-item label="性别" prop="personnelInformationGender">
        <el-input
          v-model="queryParams.personnelInformationGender"
          placeholder="请输入性别"
          clearable
          @keyup.enter.native="handleQuery"
        />
      </el-form-item>
      <el-form-item label="职务" prop="personnelInformationDuty">
        <el-input
          v-model="queryParams.personnelInformationDuty"
          placeholder="请输入职务"
          clearable
          @keyup.enter.native="handleQuery"
        />
      </el-form-item>
      <el-form-item label="部门编号" prop="personnelInformationDepartNumber">
        <el-input
          v-model="queryParams.personnelInformationDepartNumber"
          placeholder="请输入部门编号"
          clearable
          @keyup.enter.native="handleQuery"
        />
      </el-form-item>
      <el-form-item label="联系电话" prop="personnelInformationPhone">
        <el-input
          v-model="queryParams.personnelInformationPhone"
          placeholder="请输入联系电话"
          clearable
          @keyup.enter.native="handleQuery"
        />
      </el-form-item>
      <el-form-item label="登录密码" prop="personnelInformationPassword">
        <el-input
          v-model="queryParams.personnelInformationPassword"
          placeholder="请输入登录密码"
          clearable
          @keyup.enter.native="handleQuery"
        />
      </el-form-item>
      <el-form-item label="是否登录系统 0不可登录 1可登录" prop="personnelInformationLog">
        <el-input
          v-model="queryParams.personnelInformationLog"
          placeholder="请输入是否登录系统 0不可登录 1可登录"
          clearable
          @keyup.enter.native="handleQuery"
        />
      </el-form-item>
      <el-form-item label="备注" prop="personnelInformationRemark">
        <el-input
          v-model="queryParams.personnelInformationRemark"
          placeholder="请输入备注"
          clearable
          @keyup.enter.native="handleQuery"
        />
      </el-form-item>
      <el-form-item>
        <el-button type="primary" icon="el-icon-search" size="mini" @click="handleQuery">搜索</el-button>
        <el-button icon="el-icon-refresh" size="mini" @click="resetQuery">重置</el-button>
      </el-form-item>
    </el-form>

    <el-row :gutter="10" class="mb8">
      <el-col :span="1.5">
        <el-button
          type="primary"
          plain
          icon="el-icon-plus"
          size="mini"
          @click="handleAdd"
          v-hasPermi="['trace:personnel_information:add']"
        >新增</el-button>
      </el-col>
      <el-col :span="1.5">
        <el-button
          type="success"
          plain
          icon="el-icon-edit"
          size="mini"
          :disabled="single"
          @click="handleUpdate"
          v-hasPermi="['trace:personnel_information:edit']"
        >修改</el-button>
      </el-col>
      <el-col :span="1.5">
        <el-button
          type="danger"
          plain
          icon="el-icon-delete"
          size="mini"
          :disabled="multiple"
          @click="handleDelete"
          v-hasPermi="['trace:personnel_information:remove']"
        >删除</el-button>
      </el-col>
      <el-col :span="1.5">
        <el-button
          type="warning"
          plain
          icon="el-icon-download"
          size="mini"
          @click="handleExport"
          v-hasPermi="['trace:personnel_information:export']"
        >导出</el-button>
      </el-col>
      <right-toolbar :showSearch.sync="showSearch" @queryTable="getList"></right-toolbar>
    </el-row>

    <el-table v-loading="loading" :data="personnel_informationList" @selection-change="handleSelectionChange">
      <el-table-column type="selection" width="55" align="center" />
      <el-table-column label="编号" align="center" prop="personnelInformationId" />
      <el-table-column label="姓名" align="center" prop="personnelInformationName" />
      <el-table-column label="性别" align="center" prop="personnelInformationGender" />
      <el-table-column label="职务" align="center" prop="personnelInformationDuty" />
      <el-table-column label="部门编号" align="center" prop="personnelInformationDepartNumber" />
      <el-table-column label="联系电话" align="center" prop="personnelInformationPhone" />
      <el-table-column label="登录密码" align="center" prop="personnelInformationPassword" />
      <el-table-column label="是否登录系统 0不可登录 1可登录" align="center" prop="personnelInformationLog" />
      <el-table-column label="备注" align="center" prop="personnelInformationRemark" />
      <el-table-column label="操作" align="center" class-name="small-padding fixed-width">
        <template slot-scope="scope">
          <el-button
            size="mini"
            type="text"
            icon="el-icon-edit"
            @click="handleUpdate(scope.row)"
            v-hasPermi="['trace:personnel_information:edit']"
          >修改</el-button>
          <el-button
            size="mini"
            type="text"
            icon="el-icon-delete"
            @click="handleDelete(scope.row)"
            v-hasPermi="['trace:personnel_information:remove']"
          >删除</el-button>
        </template>
      </el-table-column>
    </el-table>
    
    <pagination
      v-show="total>0"
      :total="total"
      :page.sync="queryParams.pageNum"
      :limit.sync="queryParams.pageSize"
      @pagination="getList"
    />

    <!-- 添加或修改personnel_information对话框 -->
    <el-dialog :title="title" :visible.sync="open" width="500px" append-to-body>
      <el-form ref="form" :model="form" :rules="rules" label-width="80px">
        <el-form-item label="姓名" prop="personnelInformationName">
          <el-input v-model="form.personnelInformationName" placeholder="请输入姓名" />
        </el-form-item>
        <el-form-item label="性别" prop="personnelInformationGender">
          <el-input v-model="form.personnelInformationGender" placeholder="请输入性别" />
        </el-form-item>
        <el-form-item label="职务" prop="personnelInformationDuty">
          <el-input v-model="form.personnelInformationDuty" placeholder="请输入职务" />
        </el-form-item>
        <el-form-item label="部门编号" prop="personnelInformationDepartNumber">
          <el-input v-model="form.personnelInformationDepartNumber" placeholder="请输入部门编号" />
        </el-form-item>
        <el-form-item label="联系电话" prop="personnelInformationPhone">
          <el-input v-model="form.personnelInformationPhone" placeholder="请输入联系电话" />
        </el-form-item>
        <el-form-item label="登录密码" prop="personnelInformationPassword">
          <el-input v-model="form.personnelInformationPassword" placeholder="请输入登录密码" />
        </el-form-item>
        <el-form-item label="是否登录系统 0不可登录 1可登录" prop="personnelInformationLog">
          <el-input v-model="form.personnelInformationLog" placeholder="请输入是否登录系统 0不可登录 1可登录" />
        </el-form-item>
        <el-form-item label="备注" prop="personnelInformationRemark">
          <el-input v-model="form.personnelInformationRemark" placeholder="请输入备注" />
        </el-form-item>
      </el-form>
      <div slot="footer" class="dialog-footer">
        <el-button type="primary" @click="submitForm">确 定</el-button>
        <el-button @click="cancel">取 消</el-button>
      </div>
    </el-dialog>
  </div>
</template>

<script>
import { listPersonnel_information, getPersonnel_information, delPersonnel_information, addPersonnel_information, updatePersonnel_information } from "@/api/trace/personnel_information";

export default {
  name: "Personnel_information",
  data() {
    return {
      // 遮罩层
      loading: true,
      // 选中数组
      ids: [],
      // 非单个禁用
      single: true,
      // 非多个禁用
      multiple: true,
      // 显示搜索条件
      showSearch: true,
      // 总条数
      total: 0,
      // personnel_information表格数据
      personnel_informationList: [],
      // 弹出层标题
      title: "",
      // 是否显示弹出层
      open: false,
      // 查询参数
      queryParams: {
        pageNum: 1,
        pageSize: 10,
        personnelInformationId: null,
        personnelInformationName: null,
        personnelInformationGender: null,
        personnelInformationDuty: null,
        personnelInformationDepartNumber: null,
        personnelInformationPhone: null,
        personnelInformationPassword: null,
        personnelInformationLog: null,
        personnelInformationRemark: null,
      },
      // 表单参数
      form: {},
      // 表单校验
      rules: {
        personnelInformationDuty: [
          { required: true, message: "职务不能为空", trigger: "blur" }
        ],
        personnelInformationDepartNumber: [
          { required: true, message: "部门编号不能为空", trigger: "blur" }
        ],
        personnelInformationPhone: [
          { required: true, message: "联系电话不能为空", trigger: "blur" }
        ],
      }
    };
  },
  created() {
    this.getList();
  },
  methods: {
    /** 查询personnel_information列表 */
    getList() {
      this.loading = true;
      listPersonnel_information(this.queryParams).then(response => {
        this.personnel_informationList = response.rows;
        this.total = response.total;
        this.loading = false;
      });
    },
    // 取消按钮
    cancel() {
      this.open = false;
      this.reset();
    },
    // 表单重置
    reset() {
      this.form = {
        personnelInformationId: null,
        personnelInformationName: null,
        personnelInformationGender: null,
        personnelInformationDuty: null,
        personnelInformationDepartNumber: null,
        personnelInformationPhone: null,
        personnelInformationPassword: null,
        personnelInformationLog: null,
        personnelInformationRemark: null,
        createTime: null,
        updateTime: null
      };
      this.resetForm("form");
    },
    /** 搜索按钮操作 */
    handleQuery() {
      this.queryParams.pageNum = 1;
      this.getList();
    },
    /** 重置按钮操作 */
    resetQuery() {
      this.resetForm("queryForm");
      this.handleQuery();
    },
    // 多选框选中数据
    handleSelectionChange(selection) {
      this.ids = selection.map(item => item.personnelInformationId)
      this.single = selection.length!==1
      this.multiple = !selection.length
    },
    /** 新增按钮操作 */
    handleAdd() {
      this.reset();
      this.open = true;
      this.title = "添加personnel_information";
    },
    /** 修改按钮操作 */
    handleUpdate(row) {
      this.reset();
      const personnelInformationId = row.personnelInformationId || this.ids
      getPersonnel_information(personnelInformationId).then(response => {
        this.form = response.data;
        this.open = true;
        this.title = "修改personnel_information";
      });
    },
    /** 提交按钮 */
    submitForm() {
      this.$refs["form"].validate(valid => {
        if (valid) {
          if (this.form.personnelInformationId != null) {
            updatePersonnel_information(this.form).then(response => {
              this.$modal.msgSuccess("修改成功");
              this.open = false;
              this.getList();
            });
          } else {
            addPersonnel_information(this.form).then(response => {
              this.$modal.msgSuccess("新增成功");
              this.open = false;
              this.getList();
            });
          }
        }
      });
    },
    /** 删除按钮操作 */
    handleDelete(row) {
      const personnelInformationIds = row.personnelInformationId || this.ids;
      this.$modal.confirm('是否确认删除personnel_information编号为"' + personnelInformationIds + '"的数据项？').then(function() {
        return delPersonnel_information(personnelInformationIds);
      }).then(() => {
        this.getList();
        this.$modal.msgSuccess("删除成功");
      }).catch(() => {});
    },
    /** 导出按钮操作 */
    handleExport() {
      this.download('trace/personnel_information/export', {
        ...this.queryParams
      }, `personnel_information_${new Date().getTime()}.xlsx`)
    }
  }
};
</script>
